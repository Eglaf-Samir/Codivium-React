/* feedback.js  v3
 * Full-size feedback modal.
 * Four body rows: primary metrics + insight, micro-metrics, trend charts.
 * Footer: stamp left, three action buttons right.
 * No close button — buttons are the only exit.
 *
 * ============================================================
 * DESIGN: Backend computes, frontend renders
 * ============================================================
 * All insight text, chips, performance tier, and recommendations
 * are computed server-side by IFeedbackBuilder and delivered in
 * the POST /api/exercise/submit response. See:
 *   contracts/exercise-workspace-api.contract.md §"Feedback object"
 *   contracts/IFeedbackBuilder.contract.md
 *
 * The frontend only receives, formats, and renders — no threshold
 * logic or string construction here.
 *
 * ============================================================
 * Public API
 * ============================================================
 *
 *   CodiviumFeedback.show(result)
 *   CodiviumFeedback.hide()
 *
 * ============================================================
 * result shape (full — all optional fields shown)
 * ============================================================
 *
 * {
 *   // Identity (required)
 *   exerciseId:            string,   // e.g. "ex_min_window_001"
 *   exerciseName:          string,   // e.g. "Minimum Window Substring"
 *   category:              string,   // e.g. "Strings"
 *   difficulty:            string,   // "beginner" | "intermediate" | "advanced"
 *
 *   // Test outcome (required)
 *   testsPassed:           number,   // e.g. 24
 *   testsTotal:            number,   // e.g. 24
 *
 *   // Navigation
 *   returnMenuUrl:         string,   // e.g. "menu-page.html?track=micro"
 *
 *   // Current session metrics (required — client fills timeToSuccessSeconds and attempts;
 *   //                           server fills the rest via IFeedbackBuilder)
 *   current: {
 *     timeToSuccessSeconds:   number, // Total elapsed seconds from exercise open to acceptance.
 *                                     //   CLIENT measures this (elapsedSecs() in submit module).
 *     attempts:               number, // Total submissions made this session.
 *                                     //   CLIENT counts this (_attemptCount in submit module).
 *     bestPreSuccessPercent:  number, // Highest test pass% across all attempts before acceptance.
 *                                     //   SERVER computes: max(testsPassed/testsTotal * 100)
 *                                     //   over all non-accepted attempts in this session.
 *                                     //   e.g. 92 means best pre-pass was 92%.
 *     timeToFirstAttemptSecs: number, // Seconds from page load to first Submit click.
 *                                     //   SERVER computes from submission timestamps.
 *                                     //   (Or client can track this — see adapter notes.)
 *     avgIterationSeconds:    number, // Average seconds between consecutive submissions.
 *                                     //   SERVER computes: (timeToSuccessSeconds - timeToFirstAttemptSecs)
 *                                     //                    / max(1, attempts - 1)
 *   },
 *
 *   // Pre-computed insight (required for insight panel — server-generated by IFeedbackBuilder)
 *   insightText:           string,   // The insight paragraph shown in the insight panel.
 *                                    //   Server builds from threshold rules — see
 *                                    //   IFeedbackBuilder.contract.md §"Insight generation rules".
 *                                    //   Example: "First-attempt acceptance — your pre-submission
 *                                    //   reasoning was precise. Time to acceptance (12:48) was
 *                                    //   well inside the reference window for Advanced."
 *   chips:                 string[], // Short badge labels for the insight panel.
 *                                    //   Server generates from same rules.
 *                                    //   Example: ["First-try pass", "Fast solve", "Personal best"]
 *
 *   // Next exercise recommendation (optional — server provides if known)
 *   nextSuggestion:        string,   // Short sentence shown in insight chip row.
 *                                    //   Example: "Strong run — try an Advanced Strings challenge."
 *   nextExerciseId:        string,   // If provided, the "Try Again" button targets this exercise.
 *   nextExerciseName:      string,   // Display name of recommended exercise.
 *
 *   // Performance tier (optional — used for badge colour)
 *   performanceTier:       string,   // "optimal" | "good" | "acceptable" | "needs-work"
 *                                    //   Server computes — see IFeedbackBuilder.contract.md.
 *
 *   // History (optional — enables trend charts; requires 2+ entries)
 *   isFirstSolve:          boolean,  // true if no prior accepted solve for this exercise.
 *   history:               Array<{   // Past completions, oldest first (up to 8 shown).
 *     timeToSuccessSeconds: number,
 *     attempts:             number
 *   }>,
 *
 *   // Deltas vs previous solve (optional — enables delta indicators; null = first solve)
 *   deltas: {
 *     timeToSuccess: number | null,  // % change vs previous. Negative = improvement (shown green).
 *                                    //   Server computes: (current - prev) / prev * 100
 *                                    //   Example: -11 means 11% faster than last time.
 *     attempts:      number | null   // % change in attempts. Negative = fewer attempts (green).
 *   }
 * }
 *
 * ============================================================
 * Example: minimum viable call (no history, no charts)
 * ============================================================
 *
 *   CodiviumFeedback.show({
 *     exerciseId:   "ex_min_window_001",
 *     exerciseName: "Minimum Window Substring",
 *     category:     "Strings",
 *     difficulty:   "advanced",
 *     testsPassed:  24,
 *     testsTotal:   24,
 *     returnMenuUrl: "menu-page.html?track=micro",
 *     current: {
 *       timeToSuccessSeconds:   768,
 *       attempts:               2,
 *       bestPreSuccessPercent:  92,
 *       timeToFirstAttemptSecs: 125,
 *       avgIterationSeconds:    314
 *     },
 *     insightText:    "Two submissions to acceptance. Your first attempt already reached 92% of tests — one focused iteration closed the gap. Solve time (12:48) is comfortably within the expected range.",
 *     chips:          ["A2 convergence", "Best pre-pass: 92%"],
 *     nextSuggestion: "Continue: next Advanced Strings exercise.",
 *     performanceTier: "good"
 *   });
 *
 * ============================================================
 * Example: full call (with history and deltas — enables trend charts)
 * ============================================================
 *
 *   CodiviumFeedback.show({
 *     exerciseId:   "ex_min_window_001",
 *     exerciseName: "Minimum Window Substring",
 *     category:     "Strings",
 *     difficulty:   "advanced",
 *     testsPassed:  24,
 *     testsTotal:   24,
 *     isFirstSolve: false,
 *     returnMenuUrl: "menu-page.html?track=micro",
 *     current: {
 *       timeToSuccessSeconds:   768,
 *       attempts:               2,
 *       bestPreSuccessPercent:  92,
 *       timeToFirstAttemptSecs: 125,
 *       avgIterationSeconds:    314
 *     },
 *     insightText:    "Two submissions to acceptance. Your first attempt already reached 92% of tests — one focused iteration closed the gap. Time to acceptance (12:48) was well inside the reference window for Advanced — strong signal of a solid mental model for this category. 11% faster than your previous attempt — growing familiarity with this problem structure is paying off.",
 *     chips:          ["A2 convergence", "Fast solve", "Personal best time"],
 *     nextSuggestion: "Strong run — try an Advanced Strings challenge.",
 *     nextExerciseId:  "ex_group_anagrams_001",
 *     nextExerciseName: "Group Anagrams",
 *     performanceTier: "optimal",
 *     history: [
 *       { timeToSuccessSeconds: 1020, attempts: 4 },
 *       { timeToSuccessSeconds: 880,  attempts: 3 },
 *       { timeToSuccessSeconds: 960,  attempts: 3 },
 *       { timeToSuccessSeconds: 790,  attempts: 2 },
 *       { timeToSuccessSeconds: 840,  attempts: 5 },
 *       { timeToSuccessSeconds: 910,  attempts: 2 },
 *       { timeToSuccessSeconds: 860,  attempts: 3 },
 *       { timeToSuccessSeconds: 768,  attempts: 2 }
 *     ],
 *     deltas: {
 *       timeToSuccess: -11,
 *       attempts:      -33
 *     }
 *   });
 */
(function (global) {
  'use strict';

  /* ─── Display helpers ────────────────────────────────────────── */

  function fmtTime(s) {
    if (s == null) return '—';
    var m = Math.floor(s / 60);
    var sec = Math.round(s % 60);
    return m + ':' + (sec < 10 ? '0' : '') + sec;
  }

  function fmtDelta(val) {
    if (val == null) return null;
    var r = Math.round(val);
    // Negative = improvement for both time and attempts (shown green)
    var cls  = val < 0 ? 'good' : (val > 0 ? 'bad' : 'neutral');
    var sign = val > 0 ? '\u25b2 +' : '\u25bc ';
    return { text: sign + Math.abs(r) + '%', cls: cls };
  }

  function el(tag, cls, text) {
    var e = document.createElement(tag);
    if (cls)  e.className   = cls;
    if (text) e.textContent = text;
    return e;
  }

  function deltaEl(val) {
    var d = fmtDelta(val);
    if (!d) return null;
    var s = el('div', 'fb-delta ' + d.cls);
    s.textContent = d.text;
    return s;
  }

  /* ─── Chart helpers ──────────────────────────────────────────── */

  function toPath(values, w, h, pad) {
    var min = Math.min.apply(null, values);
    var max = Math.max.apply(null, values);
    var span = Math.max(1, max - min);
    var step = (w - 2 * pad) / (values.length - 1);
    var pts = values.map(function (v, i) {
      return {
        x: pad + i * step,
        y: pad + (h - 2 * pad) * (1 - (v - min) / span)
      };
    });
    var d = pts.map(function (p, i) {
      return (i === 0 ? 'M ' : 'L ') + p.x.toFixed(2) + ' ' + p.y.toFixed(2);
    }).join(' ');
    return { pts: pts, d: d, min: min, max: max };
  }

  function renderTimeChart(container, history) {
    var values = history.map(function (h) { return h.timeToSuccessSeconds; });
    if (!values.length) return;
    var w = 320, h = 110, pad = 10;
    var svg = container.querySelector('svg');
    if (!svg) return;
    var res = toPath(values, w, h, pad);
    var lineEl  = svg.querySelector('.cv-time-line');
    var areaEl  = svg.querySelector('.cv-time-area');
    var dotsEl  = svg.querySelector('.cv-time-dots');
    if (lineEl)  lineEl.setAttribute('d', res.d);
    if (areaEl)  areaEl.setAttribute('d', res.d +
      ' L ' + (w - pad).toFixed(2) + ' ' + (h - pad).toFixed(2) +
      ' L ' + pad.toFixed(2) + ' ' + (h - pad).toFixed(2) + ' Z');
    if (dotsEl) {
      dotsEl.innerHTML = res.pts.map(function (p) {
        return '<circle cx="' + p.x.toFixed(2) + '" cy="' + p.y.toFixed(2) +
          '" r="3.1" fill="rgba(246,213,138,0.95)" stroke="rgba(0,0,0,0.30)" strokeWidth="1"/>';
      }).join('');
    }
    var best   = Math.min.apply(null, values);
    var median = values.slice().sort(function(a,b){return a-b;})[Math.floor(values.length/2)];
    var latest = values[values.length-1];
    var legend = container.querySelector('.fb-legend');
    if (legend) legend.innerHTML =
      '<span>Best: ' + fmtTime(best) + '</span>' +
      '<span>Median: ' + fmtTime(median) + '</span>' +
      '<span>Latest: ' + fmtTime(latest) + '</span>';
  }

  function renderAttemptsChart(container, history) {
    var values = history.map(function (h) { return h.attempts; });
    if (!values.length) return;
    var w = 320, h = 110, pad = 10;
    var svg = container.querySelector('svg');
    if (!svg) return;
    var barsEl = svg.querySelector('.cv-att-bars');
    var lineEl = svg.querySelector('.cv-att-line');
    var max = Math.max.apply(null, values);
    var n = values.length;
    var gap = 8;
    var barW = (w - 2*pad - gap*(n-1)) / n;
    var baseY = h - pad;
    if (barsEl) {
      barsEl.innerHTML = values.map(function(a, i) {
        var x    = pad + i * (barW + gap);
        var barH = (h - 2*pad) * (a / Math.max(1, max));
        var y    = baseY - barH;
        var alpha = i === n - 1 ? 0.85 : 0.50;
        return '<rect x="' + x.toFixed(2) + '" y="' + y.toFixed(2) +
          '" width="' + barW.toFixed(2) + '" height="' + barH.toFixed(2) +
          '" rx="6" fill="rgba(246,213,138,' + alpha + ')"/>';
      }).join('');
    }
    var res = toPath(values, w, h, pad);
    if (lineEl) lineEl.setAttribute('d', res.d);
    var best   = Math.min.apply(null, values);
    var median = values.slice().sort(function(a,b){return a-b;})[Math.floor(values.length/2)];
    var latest = values[values.length-1];
    var legend = container.querySelector('.fb-legend');
    if (legend) legend.innerHTML =
      '<span>Best: ' + best + '</span>' +
      '<span>Median: ' + median + '</span>' +
      '<span>Latest: ' + latest + '</span>';
  }

  /* ─── Build modal DOM ────────────────────────────────────────── */

  function buildModal(result) {
    var c = result.current || {};
    var h = result.history || [];

    // Read pre-computed fields from server (IFeedbackBuilder)
    var insightText    = result.insightText    || '';
    var chips          = Array.isArray(result.chips) ? result.chips : [];
    var nextSuggestion = result.nextSuggestion || '';

    // Overlay
    var overlay = el('div', 'fb-overlay');
    overlay.setAttribute('role', 'dialog');
    overlay.setAttribute('aria-modal', 'true');
    overlay.setAttribute('aria-label', 'Immediate feedback');

    // Backdrop
    var backdrop = el('div', 'fb-backdrop');
    backdrop.setAttribute('aria-hidden', 'true');
    overlay.appendChild(backdrop);

    // Modal
    var modal = el('div', 'fb-modal');
    modal.id = 'cvFeedbackModal';

    /* ── HEADER ── */
    var head   = el('header', 'fb-head');
    var titleW = el('div', 'fb-title');
    var seal   = el('div', 'fb-seal');
    seal.setAttribute('aria-hidden', 'true');
    seal.innerHTML = '<svg viewBox="0 0 24 24" fill="none">' +
      '<path d="M20 6L9 17l-5-5" stroke="currentColor" strokeWidth="2.6"' +
      ' strokeLinecap="round" stroke-linejoin="round"/></svg>';

    var hgroup = el('div', 'fb-hgroup');
    hgroup.appendChild(el('div', 'fb-kicker', 'Submission accepted'));
    var nameEl = el('h2', 'fb-exercise-name', result.exerciseName || 'Exercise');
    nameEl.title = result.exerciseName || '';
    hgroup.appendChild(nameEl);

    var pills = el('div', 'fb-pills');
    pills.appendChild(el('span', 'fb-pill cat', result.category || ''));
    var lvlPill = el('span', 'fb-pill level');
    lvlPill.textContent = result.difficulty
      ? result.difficulty.charAt(0).toUpperCase() + result.difficulty.slice(1) : '';
    pills.appendChild(lvlPill);
    pills.appendChild(el('span', 'fb-pill tests',
      'Tests: ' + (result.testsPassed || 0) + ' / ' + (result.testsTotal || 0)));
    if (result.performanceTier) {
      var tierPill = el('span', 'fb-pill tier-' + result.performanceTier);
      tierPill.textContent = result.performanceTier.charAt(0).toUpperCase() +
        result.performanceTier.slice(1).replace('-', ' ');
      pills.appendChild(tierPill);
    }
    hgroup.appendChild(pills);

    titleW.appendChild(seal);
    titleW.appendChild(hgroup);
    head.appendChild(titleW);
    modal.appendChild(head);

    /* ── BODY ── */
    var body = el('div', 'fb-body');

    /* Row 1: primary metrics + insight */
    var grid2 = el('div', 'fb-grid2');

    // Primary metrics (2 cards)
    var metricsDiv = el('div', 'fb-metrics');
    metricsDiv.setAttribute('aria-label', 'Primary metrics');

    function makeCard(label, valText, unit, deltaVal, helpText) {
      var card  = el('div', 'fb-card');
      card.setAttribute('aria-label', label);
      var top   = el('div', 'fb-card-top');
      top.appendChild(el('div', 'fb-card-label', label));
      var dEl = deltaEl(deltaVal);
      if (dEl) top.appendChild(dEl);
      var metric = el('div', 'fb-metric');
      metric.appendChild(el('div', 'val', valText));
      metric.appendChild(el('div', 'unit', unit));
      card.appendChild(top);
      card.appendChild(metric);
      if (helpText) card.appendChild(el('div', 'fb-help', helpText));
      return card;
    }

    metricsDiv.appendChild(makeCard(
      'Time to Success',
      fmtTime(c.timeToSuccessSeconds), 'mm:ss',
      result.deltas ? result.deltas.timeToSuccess : null,
      'Total time from opening the exercise to first fully passing submission.'
    ));
    metricsDiv.appendChild(makeCard(
      'Attempts',
      String(c.attempts || 0), 'submissions',
      result.deltas ? result.deltas.attempts : null,
      'Number of submissions made until all tests passed.'
    ));
    grid2.appendChild(metricsDiv);

    // Insight panel — text and chips from server (IFeedbackBuilder)
    var insightDiv = el('aside', 'fb-insight');
    insightDiv.setAttribute('aria-label', 'Insight');
    var insH = el('h3', null, 'Insight');
    var insP = el('p');
    insP.textContent = insightText;
    insightDiv.appendChild(insH);
    insightDiv.appendChild(insP);

    if (chips.length || nextSuggestion) {
      var chipsRow = el('div', 'fb-chips');
      chips.forEach(function(ch) { chipsRow.appendChild(el('span', 'fb-pill', ch)); });
      if (nextSuggestion) {
        chipsRow.appendChild(el('span', 'fb-pill', '\u2192 ' + nextSuggestion));
      }
      insightDiv.appendChild(chipsRow);
    }
    grid2.appendChild(insightDiv);
    body.appendChild(grid2);

    /* Row 2: three micro-metric cards */
    var microDiv = el('div', 'fb-micro');
    microDiv.setAttribute('aria-label', 'Secondary metrics');
    microDiv.appendChild(makeCard(
      'Time to First Attempt',
      fmtTime(c.timeToFirstAttemptSecs), 'mm:ss', null, null));
    microDiv.appendChild(makeCard(
      'Best Pre-Success',
      String(c.bestPreSuccessPercent != null ? c.bestPreSuccessPercent : '—'), '% tests', null, null));
    microDiv.appendChild(makeCard(
      'Avg Iteration',
      fmtTime(c.avgIterationSeconds), 'mm:ss', null, null));
    body.appendChild(microDiv);

    /* Row 3: trend charts (only when history has 2+ entries) */
    if (h.length >= 2) {
      var chartsDiv = el('div', 'fb-charts');
      chartsDiv.setAttribute('aria-label', 'Trends over time');
      var n   = Math.min(h.length, 8);
      var sub = 'Last ' + n + ' completions';

      var timeCard = el('div', 'fb-card fb-chart');
      timeCard.setAttribute('aria-label', 'Time to success trend');
      var tHead = el('div', 'fb-chart-head');
      tHead.appendChild(el('div', 'fb-chart-title', 'Time to Success Trend'));
      tHead.appendChild(el('div', 'fb-chart-sub', sub));
      timeCard.appendChild(tHead);
      timeCard.innerHTML += '<svg viewBox="0 0 320 110" preserveAspectRatio="none" aria-label="Time trend" role="img">' +
        '<path class="cv-time-area" d="" fill="rgba(246,213,138,0.10)"></path>' +
        '<path class="cv-time-line" d="" fill="none" stroke="rgba(246,213,138,0.95)" strokeWidth="2.6" strokeLinecap="round" stroke-linejoin="round"></path>' +
        '<g class="cv-time-dots"></g></svg>';
      timeCard.appendChild(el('div', 'fb-legend'));
      chartsDiv.appendChild(timeCard);

      var attCard = el('div', 'fb-card fb-chart');
      attCard.setAttribute('aria-label', 'Attempts trend');
      var aHead = el('div', 'fb-chart-head');
      aHead.appendChild(el('div', 'fb-chart-title', 'Attempts Trend'));
      aHead.appendChild(el('div', 'fb-chart-sub', sub));
      attCard.appendChild(aHead);
      attCard.innerHTML += '<svg viewBox="0 0 320 110" preserveAspectRatio="none" aria-label="Attempts trend" role="img">' +
        '<g class="cv-att-bars"></g>' +
        '<path class="cv-att-line" d="" fill="none" stroke="rgba(245,245,252,0.72)" strokeWidth="2.4" strokeLinecap="round" stroke-linejoin="round"></path>' +
        '</svg>';
      attCard.appendChild(el('div', 'fb-legend'));
      chartsDiv.appendChild(attCard);

      body.appendChild(chartsDiv);

      setTimeout(function() {
        var slice = h.slice(-8);
        renderTimeChart(timeCard, slice);
        renderAttemptsChart(attCard, slice);
      }, 50);
    }

    modal.appendChild(body);

    /* ── FOOTER ── */
    var foot = el('footer', 'fb-foot');
    var footLeft = el('div', 'fb-foot-left');
    var stamp = el('div', 'fb-stamp');
    stamp.innerHTML = '<strong>Codivium</strong> &mdash; Immediate feedback';
    footLeft.appendChild(stamp);
    foot.appendChild(footLeft);

    var footActions = el('div', 'fb-foot-actions');
    var btnDash  = el('button', 'fb-btn ghost', 'Dashboard');
    var btnMenu  = el('button', 'fb-btn ghost', 'Return to Menu');
    // If server recommends a next exercise, "Try Again" navigates there;
    // otherwise it reloads the same exercise.
    var btnNext = el('button', 'fb-btn gold',
      result.nextExerciseId ? 'Try Next Exercise' : 'Try Again');
    btnDash.type = btnMenu.type = btnNext.type = 'button';

    btnDash.addEventListener('click', function () {
      window.location.href = 'codivium_insights_embedded.html';
    });
    btnMenu.addEventListener('click', function () {
      window.location.href = result.returnMenuUrl || 'menu-page.html';
    });
    btnNext.addEventListener('click', function () {
      var targetId   = result.nextExerciseId || result.exerciseId || '';
      var returnUrl  = result.returnMenuUrl
        ? encodeURIComponent(result.returnMenuUrl) : '';
      window.location.href = 'editor.html?id=' + encodeURIComponent(targetId) +
        (returnUrl ? '&from=' + returnUrl : '');
    });

    footActions.appendChild(btnDash);
    footActions.appendChild(btnMenu);
    footActions.appendChild(btnNext);
    foot.appendChild(footActions);
    modal.appendChild(foot);
    overlay.appendChild(modal);

    return { overlay: overlay, modal: modal };
  }

  /* ─── Cursor tracking ────────────────────────────────────────── */
  function attachTracking(modal) {
    var __fbRaf = null;
    var __fbLastE = null;
    modal.addEventListener('mousemove', function (e) {
      __fbLastE = e;
      if (__fbRaf) return;
      __fbRaf = requestAnimationFrame(function() {
        __fbRaf = null;
        if (!__fbLastE) return;
        var r = modal.getBoundingClientRect();
        modal.style.setProperty('--mx', ((__fbLastE.clientX - r.left) / r.width  * 100) + '%');
        modal.style.setProperty('--my', ((__fbLastE.clientY - r.top)  / r.height * 100) + '%');
      });
    }, { passive: true });
  }

  /* ─── Public API ─────────────────────────────────────────────── */
  var _overlay = null;

  function show(result) {
    if (_overlay) hide();
    var built = buildModal(result);
    _overlay = built.overlay;
    document.body.appendChild(_overlay);
    attachTracking(built.modal);
    requestAnimationFrame(function () { _overlay.classList.add('is-open'); });
    var focusable = _overlay.querySelectorAll('button');
    if (focusable.length) focusable[focusable.length - 1].focus();
  }

  function hide() {
    if (!_overlay) return;
    _overlay.classList.remove('is-open');
    var o = _overlay;
    setTimeout(function () {
      if (o.parentNode) o.parentNode.removeChild(o);
      if (_overlay === o) _overlay = null;
    }, 280);
  }

  global.CodiviumFeedback = { show: show, hide: hide };
})(window);
